package com.code4thought;

public class TalkCombination {

    private long binary;

    private int remainMinute;

    public TalkCombination(long binary, int remainMinute) {
        this.binary = binary;
        this.remainMinute = remainMinute;
    }

    public long getBinary() {
        return binary;
    }

    public int getRemainMinute() {
        return remainMinute;
    }
}
